# Numbers: Python has several built-in numeric data types, including integers, floating-point numbers, and complex numbers.

# Strings: Strings are sequences of characters enclosed in single or double quotes. They are used to represent text in Python.

# Booleans: Booleans are a type of data that can only take on one of two values: True or False.

# Lists: Lists are ordered sequences of elements enclosed in square brackets. They can contain elements of different types, including other lists.

# Tuples: Tuples are similar to lists but are immutable, meaning they cannot be modified once created.

# Sets: Sets are unordered collections of unique elements. They are enclosed in curly braces.

# Dictionaries: Dictionaries are key-value pairs enclosed in curly braces. They are used to store and retrieve data based on a key.

# Define some variables with different data types
my_integer = 10
my_float = 3.14
my_boolean = True
my_string = "Hello, world!"
my_list = [1, 2, 3, 4, 5]
my_tuple = (6, 7, 8, 9, 10)
my_set = {1, 2, 3, 3, 4, 5}
my_dict = {"name": "John", "age": 30, "location": "New York"}

# Print out the values of the variables
print(my_integer)   # Output: 10
print(my_float)     # Output: 3.14
print(my_boolean)   # Output: True
print(my_string)    # Output: Hello, world!
print(my_list)      # Output: [1, 2, 3, 4, 5]
print(my_tuple)     # Output: (6, 7, 8, 9, 10)
print(my_set)       # Output: {1, 2, 3, 4, 5}
print(my_dict)      # Output: {'name': 'John', 'age': 30, 'location': 'New York'}